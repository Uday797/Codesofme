package com.uday;

public class Demo3 {
	public static void main(String[] args) {
		int num=2;
		switch (num) {
		case 1:{
			System.out.println("one");
			break;
		}
		}
		switch (num) {
		case 2:{
			System.out.println("two");
			break;
		}
		
		case 3:{
			System.out.println("three");
			break;
		}
		
		default:{
		
			System.out.println("Non Valid");
		
		}
		}
	}
}